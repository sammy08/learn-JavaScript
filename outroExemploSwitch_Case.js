var moonPhase = 'full';

switch(moonPhase) {
    case 'full':
        document.write('Howwwlll!!');
        break;
    case 'mostly full':
        document.write('Arms and legs are getting hairier.');
        break;
    case 'mostly new':
        document.write('Back on two feet');
    default:
        document.write('Invalid moon phase');
        break;
}